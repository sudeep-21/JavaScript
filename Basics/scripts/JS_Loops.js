//While Loop
var a = 0;
document.writeln("<br> While: ");
while(a < 5) {
    document.writeln("<br>" + a);
    a++;
}

//Do While
var b = 0;
document.writeln("<br> Do While: ");
do {
    document.writeln("<br>" + a);
    b++;
} while(b < 5)

//For Loop
document.writeln("<br> For: ");
for(var i = 0; i < 5; i++) {
    document.writeln("<br>" + i);
}